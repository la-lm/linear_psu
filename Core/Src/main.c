    last_count_v = count_v;

    encoder_value_i += diff_i;
    last_count_i = count_i;

    Vset_mV = VMIN_mV + encoder_value_v * 10;
    if(Vset_mV < VMIN_mV) Vset_mV = VMIN_mV;
    if(Vset_mV > VMAX_mV) Vset_mV = VMAX_mV;

    Iset_mA = IMIN_mA + encoder_value_i * 5;
    if(Iset_mA < IMIN_mA) Iset_mA = IMIN_mA;
    if(Iset_mA > IMAX_mA) Iset_mA = IMAX_mA;

    float output_voltage = feedback_voltage_V * VFB_DIV_RATIO;
    float load_current_A = current_sense_voltage_V / (SHUNT_RESISTOR_mOhm * CURRENT_SENSE_AMP_GAIN);

    uint16_t dac_val_v = MapVoltageToDAC(Vset_mV);
    HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_12B_R, dac_val_v);

    uint16_t dac_val_i = MapCurrentToDAC(Iset_mA);
    HAL_DAC_SetValue(&hdac2, DAC_CHANNEL_1, DAC_ALIGN_12B_R, dac_val_i);


    char msg[128];
    char msg2[128];
    char msg3[128];
    char msg4[128];
    sprintf(msg, "Set: %d mV %d mA", Vset_mV, Iset_mA);
    LCD_write_str(msg);

    LCD_set_pos(0, 1);
    sprintf(msg2, "Act: %d mV %d mA", output_voltage, load_current_A);
    LCD_write_str(msg2);

    LCD_set_pos(0, 2);
    sprintf(msg3, "hogehoge", 0);
    LCD_write_str(msg3);

    HAL_Delay(100);


