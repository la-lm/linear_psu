void Button_Toggle_Task(void)
{
    GPIO_PinState current_button_state = HAL_GPIO_ReadPin(GPIOB, RELAY_SW_Pin);
    uint32_t current_time = HAL_GetTick();

    if (current_button_state == GPIO_PIN_RESET && last_button_state == GPIO_PIN_SET)
    {
        if ((current_time - last_button_press_time) > DEBOUNCE_DELAY_MS)
        {
            if ((current_time - LastToggleTime) >= COOLDOWN_TIME_MS)
            {
                RelayState = !RelayState;

                if (RelayState)
                {
                    HAL_GPIO_WritePin(GPIOB, TO_RELAY_DRV_Pin, GPIO_PIN_SET);
                    HAL_GPIO_WritePin(GPIOB, RELAY_LED_Pin, GPIO_PIN_SET);
                }
                else
                {
                    HAL_GPIO_WritePin(GPIOB, TO_RELAY_DRV_Pin, GPIO_PIN_RESET);
                    HAL_GPIO_WritePin(GPIOB, RELAY_LED_Pin, GPIO_PIN_RESET);
                }

                LastToggleTime = current_time;

                last_button_press_time = current_time;
            }
        }
    }

    last_button_state = current_button_state;
} 

HAL_StatusTypeDef Read_Dual_ADC_Channels(ADC_HandleTypeDef *hadc_handle, float *p_feedback_V, float *p_current_V)
{
    // 1. ADC変換を開始し、完了を待つ (ポーリング)
    // タイムアウトは適宜設定してください
    if (HAL_ADC_Start(hadc_handle) != HAL_OK)
    {
        return HAL_ERROR;
    }

    // --- (1) 帰還電圧チャネル (Rank 1) の読み取り ---
    
    // 変換完了を待つ
    if (HAL_ADC_PollForConversion(hadc_handle, 10) != HAL_OK)
    {
        HAL_ADC_Stop(hadc_handle);
        return HAL_ERROR;
    }
    // 値を取得
    raw_adc_voltage = HAL_ADC_GetValue(hadc_handle);
    
    // --- (2) 電流センスチャネル (Rank 2) の読み取り ---
    
    // 変換完了を待つ（シングルエンド設定の場合、前回のStartで次のチャネルの変換が自動的に進行しています）
    if (HAL_ADC_PollForConversion(hadc_handle, 10) != HAL_OK)
    {
        HAL_ADC_Stop(hadc_handle);
        return HAL_ERROR;
    }
    // 値を取得
    raw_adc_current = HAL_ADC_GetValue(hadc_handle);

    // ADCを停止
    HAL_ADC_Stop(hadc_handle);


    // 3. 電圧値への変換と格納
    
    // 帰還電圧チャネルの変換
    *p_feedback_V = ((float)raw_adc_voltage / (ADC_RESOLUTION)) * VOLTAGE_VREF;
    
    // 電流センスチャネルの変換
    *p_current_V = ((float)raw_adc_current / (ADC_RESOLUTION)) * VOLTAGE_VREF;

    return HAL_OK;
}
