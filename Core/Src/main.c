  HAL_TIM_Encoder_Start(&htim4, TIM_CHANNEL_ALL);
  int16_t last_count_v = __HAL_TIM_GET_COUNTER(&htim1);
  int16_t last_count_i = __HAL_TIM_GET_COUNTER(&htim4);
  int32_t encoder_value_v = 0;
  int32_t encoder_value_i = 0;

